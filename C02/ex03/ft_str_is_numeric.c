int	ft_str_is_numeric(char *str)
{
	int	i;
	int	result;

	i = 0;
	if (str[0] == '\0')
	{
		return (1);
	}
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
		{
			result = 1;
		}
		else
		{
			result = 0;
			return (result);
		}
		i++;
	}
	return (result);
}
