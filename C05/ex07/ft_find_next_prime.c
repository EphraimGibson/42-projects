int	ft_is_prime(int nb)
{
	int	i;

	i = 3;
	if (nb % 2 == 0)
		return (0);
	while (i * i <= nb)
	{
		if (nb % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	if (nb < 2)
		return (2);
	if (nb == 2147483647)
		return (nb);
	if (2147483647 - nb <= 18)
		return (2147483647);
	nb++;
	while (!ft_is_prime(nb))
		nb++;
	return (nb);
}
